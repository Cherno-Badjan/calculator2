export function getSum(addInput, addInput2) {
    return addInput + addInput2;
}
export function getDifference(subtractInput, subtractInput2) {
    return subtractInput - subtractInput2;
}
export function getProduct(multiplyInput, multiplyInput2) {
    return multiplyInput * multiplyInput2;
}
export function getDiv(divideInput, divideInput2) {
    return divideInput / divideInput2;
}